# abs(x) вычисляет абсолютное значение аргумента x. 
# По аналогии функция fabs(x) модуля math вычисляет то же значение. 
# Разница лишь в том, что math.fabs(x) возвращает число с плавающей точкой, 
# а abs(x) вернет целое число, если в качестве аргумента было целое число. 
# Fabs расшифровывается как float absolute value.